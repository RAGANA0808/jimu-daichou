/**
 * 中陰 (忌日) 計算の型定義。
 *
 * 中陰とは、命日から数えて故人が次の生を受けるまでの 49 日間 (七七日) を指す。
 * 7 日ごとに忌日 (初七日・二七日…七七日) を迎え、四十九日 (七七日) で満中陰となる。
 * 加えて、命日から 100 日目の百ヶ日 (卒哭忌) も供養の節目として広く営まれる。
 */

/** 忌日の種別。日数 (命日からの経過日数) を識別子として用いる。 */
export type ChuinKey =
  | 7 // 初七日
  | 14 // 二七日
  | 21 // 三七日
  | 28 // 四七日
  | 35 // 五七日
  | 42 // 六七日
  | 49 // 七七日 (四十九日・満中陰)
  | 100; // 百ヶ日 (卒哭忌)

/** 命日 (FULL 精度。年月日すべて判明している前提で中陰計算を行う)。 */
export type ChuinBaseDate = {
  year: number;
  month: number;
  day: number;
};

/** 1 つの忌日の算出結果。 */
export type ChuinDay = {
  /** 忌日の識別子 (命日からの経過日数)。 */
  key: ChuinKey;
  /** 表示名。例: "初七日" / "四十九日"。 */
  name: string;
  /** 別称 (あれば)。例: 四十九日 → "満中陰"、百ヶ日 → "卒哭忌"。 */
  altName: string | null;
  /** 命日からの経過日数 (命日を 1 日目と数える日本の慣習に基づく)。 */
  daysFromDeath: number;
  /** 忌日の年 (西暦)。 */
  year: number;
  /** 忌日の月。 */
  month: number;
  /** 忌日の日。 */
  day: number;
};
